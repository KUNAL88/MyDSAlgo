package com.kunal.algo.gt.traversal;

public class Weight {

    private int src_index;
    private int dst_index;
    private int weight;

    public int getSrc_index() {
        return src_index;
    }

    public void setSrc_index(int src_index) {
        this.src_index = src_index;
    }

    public int getDst_index() {
        return dst_index;
    }

    public void setDst_index(int dst_index) {
        this.dst_index = dst_index;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
